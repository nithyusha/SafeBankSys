package com.ss.entity;

public class DepartmentEmployee {
	private Integer deptUserId;
	private Integer deptId;
	private Integer userId;
	private String deptRole;
	/**
	 * @return the deptUserId
	 */
	public Integer getDeptUserId() {
		return deptUserId;
	}
	/**
	 * @param deptUserId the deptUserId to set
	 */
	public void setDeptUserId(Integer deptUserId) {
		this.deptUserId = deptUserId;
	}
	/**
	 * @return the deptId
	 */
	public Integer getDeptId() {
		return deptId;
	}
	/**
	 * @param deptId the deptId to set
	 */
	public void setDeptId(Integer deptId) {
		this.deptId = deptId;
	}
	/**
	 * @return the userId
	 */
	public Integer getUserId() {
		return userId;
	}
	/**
	 * @param userId the userId to set
	 */
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	/**
	 * @return the deptRole
	 */
	public String getDeptRole() {
		return deptRole;
	}
	/**
	 * @param deptRole the deptRole to set
	 */
	public void setDeptRole(String deptRole) {
		this.deptRole = deptRole;
	}
	
}
